a = 10
b = 5

if a > b: print("a is bigger")





# One-Line IF-ELSE (Ternary)
a = 3
b = 7

print("a wins") if a > b else print("b wins")










# Assign Value Using Ternary
age = 20

status = "Adult" if age >= 18 else "Minor"
print(status)











# Nested Ternary (If-Else Ladder)
marks = 82

grade = "A" if marks >= 90 else "B" if marks >= 75 else "C"
print(grade)
