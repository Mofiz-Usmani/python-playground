a = 10
b = 5

if a > b: print("a is bigger")





# One-Line IF-ELSE (Ternary)
a = 3
b = 7

print("a wins") if a > b else print("b wins")










# Assign Value Using Ternary
age = 20

status = "Adult" if age >= 18 else "Minor"
print(status)











# Nested Ternary (If-Else Ladder)
marks = 82

grade = "A" if marks >= 90 else "B" if marks >= 75 else "C"
print(grade)













# Multiple Statements in One IF
x = 10

if x == 10: print("Matched"); print("Correct")














# Shorthand If With Math
n = 5

message = "Even" if n % 2 == 0 else "Odd"
print(message)












# Quick Check in One Line
num = -4

print("positive") if num > 0 else print("negative")