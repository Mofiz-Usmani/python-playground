# ==================
# For Loop in Python
# ==================




# Example 1: Loop over a list
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)




# Example 2: Loop over a string
name = "Jackal"

for char in name: 
    print(char)






# Example 3: Loop with range()
# numbers from 0 to 4
for i in range(5):
    print(i)
